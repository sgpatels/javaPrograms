package BasicsOfJava;

public class UpdationVariables {
    public static void main(String[] args) {

        int x=5;
        System.out.println(x);// 5
        x=7;
        System.out.println(x);//7
        x=x+6;
        System.out.println(x);//13
        x=x-20;
        System.out.println(x);//-7
    }
}
